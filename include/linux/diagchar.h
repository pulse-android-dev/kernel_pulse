#define DATA_TYPE_F3            1
#define DATA_TYPE_LOG           2
#define DATA_TYPE_RESPONSE      3
#define DIAG_IOCTL_COMMAND_REG  0

struct bindpkt_params {
	uint16_t cmd_code;
	uint16_t subsys_id;
	uint16_t cmd_code_lo;
	uint16_t cmd_code_hi;
	uint16_t proc_id;
	uint32_t event_id;
	uint32_t log_code;
	uint32_t client_id;
};

#define MAX_SYNC_OBJ_NAME_SIZE 32

